    public String getInputString(String property, boolean mandatory) {
        do {
            try {
                String name = (String) JOptionPane.showInputDialog(rootPane, "Enter " + property + ": ");
                if (mandatory && name != null && !name.isEmpty()) {
                    return name;
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, e.toString(), "Error Occured", JOptionPane.ERROR_MESSAGE);
                continue;
            }
        } while (mandatory);
        return "";
    }

    public int getInputInt(String property, boolean mandatory) {
        do {
            try {
                int data = Integer.parseInt((String) JOptionPane.showInputDialog(rootPane, "Enter " + property + ": "));
                if (mandatory) {
                    return data;
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(rootPane, "Enter int value", "Error Occured", JOptionPane.ERROR_MESSAGE);
                continue;
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, e.toString(), "Error Occured", JOptionPane.ERROR_MESSAGE);
                continue;
            }
        } while (mandatory);
        return -1;
    }
