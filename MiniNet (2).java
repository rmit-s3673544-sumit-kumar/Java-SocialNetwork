    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            String name = this.getInputString("Name", true);
            int age = this.getInputInt("Age", true);
            String status = this.getInputString("Status", false);
            String address = this.getInputString("Address", false);
            String gender = this.getInputString("Gender", false);
            String profilePic = this.getInputString("Profile Photo", false);
            User user = Driver.createUser(name, age);
            user.setAddress(address);
            user.setGender(gender);
            user.setStatus(status);
            user.setUserImage(profilePic);
            if (user instanceof ChildUser || user instanceof YoungChild) {
                while (true) {
                    this.showInfoMessage("Enter first parent details", "Details");
                    try {
                        name = this.getInputString("Name", true);
                        User parentUser = Driver.getUserFromName(name);
                        Driver.connectPeople(user, parentUser, ConnectionType.PARENT);
                        break;
                    } catch (Exception ex) {
                        this.showErrorMessage("Error: " + ex.getMessage(), "Error");
                        continue;
                    }
                }
                while (true) {
                    this.showInfoMessage("Enter second parent details", "Details");
                    try {
                        name = this.getInputString("Name", true);
                        User parentUser = Driver.getUserFromName(name);
                        Driver.connectPeople(user, parentUser, ConnectionType.PARENT);
                        break;
                    } catch (Exception ex) {
                        this.showErrorMessage("Error: " + ex.getMessage(), "Error");
                        continue;
                    }
                }
            }
            driver.addPerson(user);
            this.showInfoMessage("User " + user.getName() + " added successfully.", "Added");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.toString(), "Error Occured", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

